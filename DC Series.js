function showSeriesDetails(seriesId) {
    const series = seriesData[seriesId];
    document.getElementById('series-title').innerText = series.title;
    document.getElementById('series-description').innerText = series.description;

    const seasonList = document.getElementById('season-list');
    seasonList.innerHTML = '';
    series.seasons.forEach((season, index) => {
        const li = document.createElement('li');
        li.innerText = `Season ${index + 1}`;
        li.onclick = () => showSeasonDetails(seriesId, index);
        seasonList.appendChild(li);
    });

    // Show season list and hide the series list
    document.getElementById('seasons-list').style.display = 'block';
    document.getElementById('series-list').style.display = 'none';
}

function showSeasonDetails(seriesId, seasonIndex) {
    const series = seriesData[seriesId];
    const season = series.seasons[seasonIndex];
    document.getElementById('season-title').innerText = `Season ${seasonIndex + 1}`;

    const episodeList = document.getElementById('episode-list');
    episodeList.innerHTML = '';
    season.episodes.forEach((episode, index) => {
        const li = document.createElement('li');
        li.innerText = episode.title;
        li.onclick = function() {
            showEpisodeDetails(seriesId, seasonIndex, index);
        };
        episodeList.appendChild(li);
    });

    // Show episode list and hide the season list
    document.getElementById('episodes-list').style.display = 'block';
    document.getElementById('seasons-list').style.display = 'none';
}

function showEpisodeDetails(seriesId, seasonIndex, episodeIndex) {
    const series = seriesData[seriesId];
    const episode = series.seasons[seasonIndex].episodes[episodeIndex];
    document.getElementById('episode-title').innerText = episode.title;
    const video = document.getElementById('series-video');
    const videoSource = document.getElementById('series-video-source');
    videoSource.src = episode.videoSrc;
    video.load();  // Reload the video to apply the new source

    // Show movie details and hide the episode list
    document.getElementById('series-details').style.display = 'block';
    document.getElementById('episodes-list').style.display = 'none';
}

