function searchSeries() {
    const query = document.getElementById('searchInput').value.toLowerCase();
    const seriesList = document.getElementById('series-list');
    const seriesItems = seriesList.getElementsByClassName('series-item');
    let foundItems = [];

    for (let i = 0; i < seriesItems.length; i++) {
        const item = seriesItems[i];
        const title = item.getElementsByTagName('p')[0].innerText.toLowerCase();
        if (title.includes(query)) {
            foundItems.push(item.parentElement); // parentElement refers to the <section class="part">
            item.style.display = 'block';
        } else {
            item.style.display = 'none';
        }
    }

    const parts = seriesList.getElementsByClassName('part');
    for (let part of parts) {
        part.style.display = 'none';
    }

    for (let foundItem of foundItems) {
        foundItem.style.display = 'block';
    }
}

function resetSearch() {
    const seriesList = document.getElementById('series-list');
    const seriesItems = seriesList.getElementsByClassName('series-item');

    for (let i = 0; i < seriesItems.length; i++) {
        seriesItems[i].style.display = 'block';
    }

    const parts = seriesList.getElementsByClassName('part');
    for (let part of parts) {
        part.style.display = 'block';
    }

    document.getElementById('searchInput').value = '';
}